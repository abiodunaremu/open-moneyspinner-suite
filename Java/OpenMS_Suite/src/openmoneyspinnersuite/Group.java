    private void jGroupRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGroupRegisterButtonActionPerformed
        // TODO add your handling code here:
        if(jGroupTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Group Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            /* Execute submit thread */
            processThread=new Thread(new Runnable() {
                public void run() {
                disableControls();
                submitData();
                reset();
                }
            });
            titleThread=new Thread(new Runnable() {
                public void run() {
                    while(processThread.isAlive())setProcessingTitle(titleThread);
                    enableControls();
                    System.out.println("viewItemThread stopped runing.");
                }
            });
            processThread.start();
            titleThread.start();
        }
    }//GEN-LAST:event_jGroupRegisterButtonActionPerformed
                
    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Group - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Group - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Group - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Group - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(processThread.isAlive())
        {
            try{
                processThread.stop();
                titleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. New Group");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    private void disableControls()
    {
        jGroupTextField.setEnabled(false);
        jGroupRegisterButton.setEnabled(false);
    }
    private void enableControls()
    {
        jGroupTextField.setEnabled(true);
        jGroupRegisterButton.setEnabled(true);
        this.setTitle("::. New Group");
    }
    private void jGroupTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jGroupTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jGroupTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jGroupTextField.setText(newValue);
        }
    }//GEN-LAST:event_jGroupTextFieldKeyTyped

     void reset()
    {
        jGroupTextField.setText("");
    }

     void submitData()
    {
        //Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+" prnInsGroup"+Connect.procInitStart+"?"+Connect.procInitEnd);
            try{
            statement.setString(1,jGroupTextField.getText());
            }
            catch(Exception e)
            {
            System.out.println("Group.submitData() gave error ! "+e);
            }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }

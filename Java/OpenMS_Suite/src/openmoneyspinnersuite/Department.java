    private void jDepartmentRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDepartmentRegisterButtonActionPerformed
        // TODO add your handling code here:
        if(jDepartmentTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Department Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            /* Execute submit thread */
            departmentThread=new Thread(new Runnable() {
                public void run() {
                    disableControls();
                    submitData();
                    reset();
                }
            });
            departmentTitleThread=new Thread(new Runnable() {
                public void run() {
                    while(departmentThread.isAlive())setProcessingTitle(departmentTitleThread);
                    enableControls();
                    System.out.println("departmentThread stopped runing.");
                }
            });
            departmentThread.start();
            departmentTitleThread.start();
        }
    }//GEN-LAST:event_jDepartmentRegisterButtonActionPerformed
                              
    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Department - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Department - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Department - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Department - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println();}
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(departmentThread.isAlive())
        {
            try{
                departmentThread.stop();
                departmentTitleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. New Account");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jDepartmentTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jDepartmentTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jDepartmentTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jDepartmentTextField.setText(newValue);
        }
    }//GEN-LAST:event_jDepartmentTextFieldKeyTyped

     void reset()
    {
        jDepartmentTextField.setText("");
    }
    private void disableControls()
    {
        jDepartmentTextField.setEnabled(false);
        jDepartmentTextField.setEditable(false);
        jDepartmentRegisterButton.setEnabled(false);
    }
    private void enableControls()
    {
        jDepartmentTextField.setEnabled(true);
        jDepartmentTextField.setEditable(true);
        jDepartmentRegisterButton.setEnabled(true);
        this.setTitle("::. New Department");
    }

     void submitData()
    {
        //Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+" prnInsDepartment"+Connect.procInitStart+"?"+Connect.procInitEnd);
            try{
            statement.setString(1,jDepartmentTextField.getText());
            }
            catch(Exception e)
            {
            System.out.println("Department.submitData() gave error ! "+e);
            }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }

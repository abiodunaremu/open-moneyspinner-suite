    private void jTTRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTTRegisterButtonActionPerformed
        
        /* Execute thread */
        processThread=new Thread(new Runnable() {
            public void run() 
            {
                disableControls();
                if(jTransactionTypeTextField.getText().trim().isEmpty())
                {
                    JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Transaction Type ","ERROR !",JOptionPane.ERROR_MESSAGE);
                    return;
                }
                else
                {
                    submitData();
                    reset();
                }
            }
        });
        titleThread=new Thread(new Runnable() {
            public void run() {
                while(processThread.isAlive())setProcessingTitle(titleThread);
                enableControls();
                System.out.println("viewIPDebitThread stopped runing.");
            }
        });
        processThread.start();
        titleThread.start();

    }//GEN-LAST:event_jTTRegisterButtonActionPerformed

    private void jCancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCancelButtonActionPerformed
        
        if(processThread.isAlive())
        {
            try{
                processThread.stop();
                titleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. New Programme");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jCancelButtonActionPerformed

    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Transaction Type - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Transaction Type - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Transaction Type - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Transaction Type - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
            this.setTitle("::. New Transaction Type");
    }
    private void jTransactionTypeTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTransactionTypeTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jTransactionTypeTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jTransactionTypeTextField.setText(newValue);
        }

    }//GEN-LAST:event_jTransactionTypeTextFieldKeyTyped
    void reset()
    {
        jTransactionTypeTextField.setText("");
    }
    private void disableControls()
    {
        jTransactionTypeTextField.setEnabled(false);
        jTTRegisterButton.setEnabled(false);
    }
    private void enableControls()
    {
        jTransactionTypeTextField.setEnabled(true);
        jTTRegisterButton.setEnabled(true);
        this.setTitle("::. New Transaction Type");
    }
    void submitData()
    {
        //Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+" prnInsTransactionType"+Connect.procInitStart+"?"+Connect.procInitEnd);
        try
        {
            statement.setString(1,jTransactionTypeTextField.getText());
        }
        catch(Exception e)
        {
            System.out.println("TransactionType.submitData() gave error ! "+e);
        }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }


    private void jItemUnitRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jItemUnitRegisterButtonActionPerformed
        // TODO add your handling code here:
        if(jItemUnitTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Item Unit Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
        /* Execute submit thread */
        processThread=new Thread(new Runnable() {
            public void run() {
            disableControls();
            submitData();
            reset();
            }
        });
        titleThread=new Thread(new Runnable() {
            public void run() {
                while(processThread.isAlive())setProcessingTitle(titleThread);
                enableControls();
                System.out.println("viewItemUnitThread stopped runing.");
            }
        });
        processThread.start();
        titleThread.start();
        }
    }//GEN-LAST:event_jItemUnitRegisterButtonActionPerformed

    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Item Unit - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Item Unit - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Item Unit - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Item Unit - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(processThread.isAlive())
        {
            try{
                processThread.stop();
                titleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. Item Category");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jItemUnitTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jItemUnitTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jItemUnitTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jItemUnitTextField.setText(newValue);
        }
    }//GEN-LAST:event_jItemUnitTextFieldKeyTyped

     void reset()
    {
        jItemUnitTextField.setText("");
    }

     private void disableControls()
     {
        jItemUnitTextField.setEnabled(false);
        jItemUnitRegisterButton.setEnabled(false);
     }
     private void enableControls()
     {
        jItemUnitTextField.setEnabled(true);
        jItemUnitRegisterButton.setEnabled(true);
        this.setTitle("::. New Item Unit");
     }
     void submitData()
    {
        //Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+"  prnInsItemUnit"+Connect.procInitStart+"?"+Connect.procInitEnd);
            try{
            statement.setString(1,jItemUnitTextField.getText());
            }
            catch(Exception e)
            {
            System.out.println("ItemUnit.submitData() gave error ! "+e);
            }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }

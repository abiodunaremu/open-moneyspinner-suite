    private void jItemCategoryRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jItemCategoryRegisterButtonActionPerformed

        /* Execute thread */
        processThread=new Thread(new Runnable() {
            public void run() {
                disableControls();
                if(jItemLocationTextField.getText().trim().isEmpty())
                {
                    JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Item Location Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
                    return;
                }
                else
                {
                    submitData();
                    reset();
                }
            }
        });
        titleThread=new Thread(new Runnable() {
            public void run() {
                while(processThread.isAlive())setProcessingTitle(titleThread);
                enableControls();
                System.out.println("viewItemThread stopped runing.");
            }
        });
        processThread.start();
        titleThread.start();
    }//GEN-LAST:event_jItemCategoryRegisterButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(processThread.isAlive())
        {
            try{
                processThread.stop();
                titleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. New Item Location");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jItemLocationTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jItemLocationTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jItemLocationTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jItemLocationTextField.setText(newValue);
        }
    }//GEN-LAST:event_jItemLocationTextFieldKeyTyped

    public void reset()
    {
        jItemLocationTextField.setText("");
    }

                                                
    private void disableControls()
    {
        jItemCategoryRegisterButton.setEnabled(false);
    }                                             
    private void enableControls()
    {
        jItemCategoryRegisterButton.setEnabled(true);
        this.setTitle("::. New Item Location");
    }
                   
    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Item Location - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Item Location - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Item Location - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Item Location - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
    }
    void submitData()
    {
        Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+"  prnInsItemLocation"+Connect.procInitStart+"?"+Connect.procInitEnd);
        try
        {
            statement.setString(1,jItemLocationTextField.getText());
        }
        catch(Exception e)
        {
            System.out.println("ItemLocation.submitData() gave error ! "+e);
        }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }

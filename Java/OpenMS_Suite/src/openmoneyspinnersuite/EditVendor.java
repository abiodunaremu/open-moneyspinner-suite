    private void vendorIDSelected()
    {
        String vendorID=vendorIDComboBox.getSelectedItem().toString();
        if(vendorID.equals("-- Select --"))
        {
            resetVendorID();
            resetFields();            
        }
        else
        {
            try
            {
                //Connect.createMSSQLConnection();
                Connect.changeDB(OpenMSApp.Database_A);
                statement=Connect.createStatement("Select * from Vendor where cVendorID= '"+vendorID+"'");
                result=Connect.executeRStatement(statement);
                result.next();
                dbName=result.getString(2).trim();
                dbAddress=result.getString(3).trim();
                dbCity=result.getString(4).trim();
                dbState=result.getString(5).trim();
                dbCountryCode=result.getString(6).trim();
                dbZip=result.getString(7).trim();
                dbPhoneNo=result.getString(8).trim();
                dbRegNo=result.getString(9).trim();

                Connect.closeConnection();
            }
            catch(Exception e)
            {
                System.out.println("Sql errror! at EditVendor.vendorIDComboBoxActionPerformed "+e);
            }

            dbCountry=systems.getValue(OpenMSApp.Database_A,"Select * from country where cCountryCode= '"+dbCountryCode+"'","vCountryName");

            jAddressTextArea.setText(dbAddress);
            jNameTextField.setText(dbName);
            jCityTextField.setText(dbCity);
            jStateTextField.setText(dbState);
            jRegNoTextField.setText(dbRegNo);
            jZipTextField.setText(dbZip);
            jPhoneTextField.setText(dbPhoneNo);
            jCountryComboBox.setSelectedItem(dbCountry);
        }
            disableFields();
            resetCheckBoxes();
    }

    private void jUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateButtonActionPerformed
        // TODO add your handling code here:

        if(vendorIDComboBox.getSelectedItem().equals("-- Select --"))
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Select a Vendor ID ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(isCheckBoxesFalse()==false)
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame()," No Data Has Been Edited ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }

        String value=jPhoneTextField.getText().trim();
        int x=value.length();
        int y=0;
        char[] ch=value.toCharArray();

        if(jNameTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter Vendor's Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jAddressTextArea.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter Vendor's Address ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jPhoneTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter Vendor's Phone Number ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }


        while(y<x)
        {
        try
        {
            if(Character.isDigit(ch[y]))
            {}
            else{
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Vendor's Phone Number Must Be in Digits","ERROR !",JOptionPane.ERROR_MESSAGE);
            return; }
        }
        catch(Exception e)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Phone Number Can Not Contain Alphabets","ERROR !",JOptionPane.ERROR_MESSAGE);
        jPhoneTextField.setText("");
        System.out.println(e+" Non int available in "+value+" non int is "+ch[y]);
        return;
        }
        y++;
        }


        if(jCityTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter Vendor's City ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jStateTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter Vendor's State ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jCountryComboBox.getSelectedItem().equals("-- Select --"))
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Select Vendor's Country ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }

        else
        {
        /* Execute submit thread */
        editVendorThread=new Thread(new Runnable() {
            public void run() {
                disableControls();
                submitData();
                resetVendorID();
                resetFields();
                resetCheckBoxes();
                disableFields();
            }
        });
        editVendorTitleThread=new Thread(new Runnable() {
            public void run() {
                while(editVendorThread.isAlive())setProcessingTitle(editVendorTitleThread);
                enableControls();
                System.out.println("editVendorThread stopped runing.");
            }
        });
        editVendorThread.start();
        editVendorTitleThread.start();
        }
    }//GEN-LAST:event_jUpdateButtonActionPerformed
                  
    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. Edit Vendor - Processing Request");
            thread.sleep(500);
            this.setTitle("::. Edit Vendor - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. Edit Vendor - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. Edit Vendor - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(editVendorThread.isAlive())
        {
            try{
                editVendorThread.stop();
                editVendorTitleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. Edit Vendor");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed


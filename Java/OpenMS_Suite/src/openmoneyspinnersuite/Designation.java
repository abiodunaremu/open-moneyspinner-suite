    private void jDesignationRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDesignationRegisterButtonActionPerformed
        // TODO add your handling code here:
        if(jDesignationTextField.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Please Enter a Designation Name ","ERROR !",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            /* Execute submit thread */
            designationThread=new Thread(new Runnable() {
                public void run() {
                    disableControls();
                    submitData();
                    reset();
                }
            });
            designationTitleThread=new Thread(new Runnable() {
                public void run() {
                    while(designationThread.isAlive())setProcessingTitle(designationTitleThread);
                    enableControls();
                    System.out.println("accountThread stopped runing.");
                }
            });
            designationThread.start();
            designationTitleThread.start();
        }
    }//GEN-LAST:event_jDesignationRegisterButtonActionPerformed
                             
    private void setProcessingTitle(Thread thread)
    {
        try{
            this.setTitle("::. New Designation - Processing Request");
            thread.sleep(500);
            this.setTitle("::. New Designation - Processing Request.");
            thread.sleep(500);
            this.setTitle("::. New Designation - Processing Request..");
            thread.sleep(500);
            this.setTitle("::. New Designation - Processing Request...");
            thread.sleep(500);
        }catch(Exception e){System.out.println("Title thread"+e);}
    }
    private void jDesignationTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jDesignationTextFieldKeyTyped
        // TODO add your handling code here:
        String value=jDesignationTextField.getText();
        if(value.length()>49)
        {
        JOptionPane.showMessageDialog(JOptionPane.getRootFrame(),"Maximum Number of Character (50) Exceeded ","ERROR !",JOptionPane.ERROR_MESSAGE);
        String newValue=String.copyValueOf(value.toCharArray(),0, 49);
        jDesignationTextField.setText(newValue);
        }
    }//GEN-LAST:event_jDesignationTextFieldKeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(designationThread.isAlive())
        {
            try{
                designationThread.stop();
                designationTitleThread.stop();
                System.out.println("Threads successfully stopped.");
            }catch(Exception e){System.out.println("Thread stopping error: "+e);}
            this.setTitle("::. New Designation");
            enableControls();
        }
        else
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

     void reset()
    {
    jDesignationTextField.setText("");
    }
    private void disableControls()
    {
        jDesignationTextField.setEnabled(false);
        jDesignationTextField.setEditable(false);
        jDesignationRegisterButton.setEnabled(false);
    }
    private void enableControls()
    {
        jDesignationTextField.setEnabled(true);
        jDesignationTextField.setEditable(true);
        jDesignationRegisterButton.setEnabled(true);
        this.setTitle("::. New Designation");
    }

     void submitData()
    {
        //Connect.createMSSQLConnection();
        Connect.changeDB(OpenMSApp.Database_A);
        statement=Connect.createStatement(Connect.procInit+"  prnInsDesignation"+Connect.procInitStart+"?"+Connect.procInitEnd);
            try{
            statement.setString(1,jDesignationTextField.getText());
            }
            catch(Exception e)
            {
            System.out.println("Designation.submitData() gave error ! "+e);
            }
        Connect.alert="Y";
        Connect.executeUpdateStatement(statement);
        Connect.closeConnection();
    }
